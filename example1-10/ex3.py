"""
练习 3 数字和数学 
+ plus，加号
- minus，减号
/ slash，斜杠
* asterisk，星号
% percent，百分号
< less-than，小于号
> greater-than，大于号
<= less-than-equal，小于等于号
>= greater-than-equal，大于等于号
"""

print("I will now count my chickens:") # 我现在数一下我的鸡

print("Hens", 25 + 30 / 6) # 母鸡: 30只
print("Roosters", 100 - 25 * 3 % 4) # 公鸡: 97只

print("Now I will count the eggs:") # 我现在数下我的鸡蛋

print(3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6) # 6.75

print("Is it true that 3 + 2 < 5 - 7?")

print(3 + 2 < 5 - 7) # False

print("What is 3 + 2?", 3 + 2) # 5
print("What is 5 - 7?", 5 - 7) # -2

print("Oh, that's why it's False.")

print("How about some more.")

print("Is it greater?", 5 > -2) # True
print("Is it greater or equal?", 5 >= -2) # 大还是相等 True
print("Is it less or equal?", 5 <= 2) # 小还是相等 False
